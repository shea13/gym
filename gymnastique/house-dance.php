Différentes danses sont à l'origine de la house dance. Ses principaux précurseurs furent le jacking’, le footwork et le lofting. Le jacking’ est né dans le courant des années 1970-1980 sur la côte Est américaine. Il existe quantité de morceaux qui y font référence comme « Time to Jack », « Jack your Body », « Jack the house » mais également des noms d’artistes, « Farley Jackmaster Funk », Jackmaster Dick », … . A New-York, le jacking’ était également appelé hip-house en référence aux danseurs de hip hop qui dansaient sur de la house music dans les clubs New-Yorkais.
Il existe également d’autres styles, tels que le wacking, le hustle, le locking ou encore le loft style.</p><br/><br/>
