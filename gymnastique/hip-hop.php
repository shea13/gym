Le <strong>hip-hop</strong> est un mouvement culturel et artistique apparu &agrave; New York, dans le South Bronx au d&eacute;but des ann&eacute;es 1970, qui mêle des aspects festifs et revendicatifs. Issu des ghettos noirs de New York, il se r&eacute;pandra rapidement dans l'ensemble du pays puis au monde entier pour devenir une culture urbaine importante qui se décline en quatre principales disciplines : le deejaying, le rap, le b-boying et le graffiti qui, bien que né avant le <strong>hip hop</strong>, il lui fut int&eacute;gr&eacute; d&egrave;s la naissance du mouvement.
<br/>
<br/><p>La danse <strong>hip-hop</strong> apparaît avec le breakdance (terme employé par le médias), une danse au sol caractérisée par son aspect acrobatique et ses figures et qui s'inspire en partie des funk styles (Locking, Popping et Boogaloo).Mais le terme correct pour parler de cette danse est le b-boying. Les danseurs sont également appelés breakers ou B-Boys, B-Girls pour les danseuses,(le nom de « b-boy » n’a pas de signification précise, on parlera de bronx-boy, bad-boy, break-bole) . De nouveaux styles sont apparus comme la house dance, le new style, le hooba lumps, le step-up ou encore le krump.</p>
<br/><p>Ces danses, et principalement la breakdance, sont ponctuées par de nombreuses battles, ainsi la violence n'est pas physique mais artistique, ou sort vainqueur du « conflit » le meilleur des deux. Les battles peuvent aussi se faire groupe contre groupe, et les meilleurs vont à la compétition la plus prestigieuse pour les breakeurs : la Battle Of The Year (BOTY).</p>
